// operadores aritimeticos


// . + (adição / concatenação)
const num1 = 5; 
const num2 = 10; 
console.log(num1 + num2);

// ++ operador de encremento;
let num13 = 5; 
console.log(++num13);

// . - (subtração)
const num3 = 5; 
const num4 = 10; 
console.log(num3 - num4);

// -- operador de decremento 
let num14 = 5; 
console.log(--num14);

// . / (divição)
const num5 = 5; 
const num6= 10; 
console.log(num5 / num6);

// * (multiplicação) 
const num7 = 5;
const num8 = 10;
console.log(num7 * num8);

// ** (elevação numerica)
const num9 = 5;
const num10 = 10;
console.log(num9 ** num10);

// % retorna o reto da divisao
const num11 = 10;
const num12 = 3;
console.log(num11 % num12);

// encremento facil isso pode ser feito com qualquer operador
let contador = 0;
contador += 2;
contador *= 2;
contador += 2;
console.log(contador);

// convertendo string em um numero
const num20 = Number('5');
const num21 = 10;
const soma = num20 + num21;
console.log(soma);
console.log(typeof num20);

